package gov.sc.form;

public class FileInfo {
	protected String reFile;
	protected String tarSave;
	protected String tarColumn;
	public String getReFile() {
		return reFile;
	}

	public void setReFile(String reFile) {
		this.reFile = reFile;
	}

	public String getTarSave() {
		return tarSave;
	}

	public void setTarSave(String tarSave) {
		this.tarSave = tarSave;
	}

	public String getTarColumn() {
		return tarColumn;
	}

	public void setTarColumn(String tarColumn) {
		this.tarColumn = tarColumn;
	}

}
